package com.contornos;

public class Mates {

    public static int dividirStatic(int a, int b) {
        return a / b;
    }
    public static int sumarStatic(int a, int b) {
        return a + b;
    }
    public int restar(int a, int b) {
        return a - b;
    }
    public static int multiplicarStatic(int a, int b){
        return a * b;
    }


}
